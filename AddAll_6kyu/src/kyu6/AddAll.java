package kyu6;
import java.util.*;

/**
 *
 * @author stefano
 */
public class AddAll {
    public static int addAll(final int[] numbers) {
        int result = 0;
        List<Integer> costs = new ArrayList<Integer>();
        
        for (int value: numbers) {
            costs.add(value);
        }
        
        return result;
    }
    
}
